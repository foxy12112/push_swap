#include "../push_swap.h"

int	main(int argc, char *argv[])
{
	int		o;
	int		i;
	int		k;

	k = 1;
	o = 0;
	i = 0;
	while (i < argc)
	{
		o += ft_wordcount(argv[k], ' ');
		k++;
		i++;
	}
	printf("%d\n", o);
	return (0);
}
